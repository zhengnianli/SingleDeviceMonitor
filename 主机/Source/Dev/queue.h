#ifndef _QUEUE_H
#define _QUEUE_H

typedef struct 												// 队列结构体
{
	char *psBuff;												// 队列数据区			
	unsigned int unSize;										// 队列数据长度
	unsigned int unFront;										// 队列头指针
	unsigned int unBack;										// 队列尾指针
	unsigned int unResidue;									// 队列已用空间
}Queue_t ;

/*******************************************************************************************************
** 函数: QueueInit, 串口队列初始化
**------------------------------------------------------------------------------------------------------
** 参数: *buf数据缓存、Size 缓存空间、*Queue  队列指针
** 返回: 无
********************************************************************************************************/
void QueueInit( char *psBuf, unsigned int Size, Queue_t *ptQueue ) ;

/*******************************************************************************************************
** 函数: QueueInPut, 队列写函数
**------------------------------------------------------------------------------------------------------
** 参数: pdata  数据、 *Queue 队列指针
** 返回: 队列是否已满
********************************************************************************************************/
int QueueInPut( char cData,  Queue_t *ptQueue ) ;

/*******************************************************************************************************
** 函数: QueueOutPut, 队列读函数
**------------------------------------------------------------------------------------------------------
** 参数: *pdata 数据指针、 *Queue 队列指针
** 返回: 队列是否已空
********************************************************************************************************/
int QueueOutPut( char *pcData, Queue_t *ptQueue ) ;

/*******************************************************************************************************
** 函数: Queuels, 队列状态检测
**------------------------------------------------------------------------------------------------------
** 参数: QueueRx  数据指针
** 返回: 队列已用空间
********************************************************************************************************/
#define Queuels( tQueueRx ) tQueueRx.unResidue

#endif

