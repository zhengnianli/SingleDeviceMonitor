#ifndef __SYSTICK_H__
#define __SYSTICK_H__

/*******************************************************************************************************
** 函数: SoftTmrInit, 软件定时器的初始化
**------------------------------------------------------------------------------------------------------
** 参数: 无
** 返回: 无
********************************************************************************************************/
void SysTickInit(void);								

/*******************************************************************************************************
** 函数: SysTickDelayMS, ms级延迟，延迟精度为正负1ms
**------------------------------------------------------------------------------------------------------
** 参数: n : 延迟长度，单位1 ms。 n 应大于2
** 返回: 无
********************************************************************************************************/
void SysTickDelayMS(unsigned long  n);

/*******************************************************************************************************
** 函数: GetRunTime, 获取CPU运行时间，单位1ms
**------------------------------------------------------------------------------------------------------
** 参数: 无
** 返回: CPU运行时间，单位1ms
********************************************************************************************************/
unsigned long  GetRunTime(void);	   								

/*******************************************************************************************************
** 函数: GetRand, 获得随机数
**------------------------------------------------------------------------------------------------------
** 参数: 无
** 返回: 
********************************************************************************************************/
int  GetRand( void );										


#endif
